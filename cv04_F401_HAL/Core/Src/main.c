int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */

  sct_init();

  //Function HAL_ADCEx_Calibration_Start() not found in library "stm32f4xx_hal.h"
  //HAL_ADCEx_Calibration_Start(&hadc1);
  HAL_ADC_Start_IT(&hadc1);

  uint32_t tim_default = HAL_GetTick();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  static enum { SHOW_POT, SHOW_VOLT, SHOW_TEMP } state = SHOW_POT;

	  //Read states of buttons
	  uint8_t btn = Buttons();

	  if(btn == 0x1){
		  state = SHOW_VOLT;
		  tim_default = HAL_GetTick();
	  }
	  else if(btn == 0x2){
		  state = SHOW_TEMP;
		  tim_default = HAL_GetTick();
	  }
	  else if(HAL_GetTick() > tim_default + SHOW_PERIOD){
		  state = SHOW_POT;
	  }

	  int32_t temperature;

	  switch(state)
	  {
	  	  case SHOW_POT:
	  		  sct_value(raw_pot * 500 / 4096, raw_pot * 9 / 4096);
	  		  break;
		  case SHOW_VOLT:
			  sct_value(330 * (*VREFINT_CAL_ADDR) / raw_volt, raw_volt * 9 / 4096);
			  break;
		  case SHOW_TEMP:
		  	  temperature = (raw_temp - (int32_t)( *TEMP30_CAL_ADDR));
		  	  temperature = temperature * (int32_t)(110 - 30);
		  	  temperature = temperature / (int32_t)(*TEMP110_CAL_ADDR - *TEMP30_CAL_ADDR);
		  	  temperature = temperature + 30;
		  	  sct_value(temperature, raw_temp * 9 / 4096);
		  	  break;

	  }
  }
  /* USER CODE END 3 */
}

